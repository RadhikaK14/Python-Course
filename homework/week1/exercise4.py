# Push:
# Uploads all the local branch commits to the remote branch. 
# It basically updates or publishes.
# ne of the 4 commands in GIT that prompts interaction with remote repo.

# Commit: 
# It saves the changes to the local repo. It creates commit (Snapshot of the repo)
# Commits must be made as and when we make any logical changes to our code.
# Over time, commits should tell a story of the history of our repo.

# Pull:
# updates our local repo or branch with commits from the remote repo or branch.
# Combination of fetch + merge
# it is a good idea to run status before pull, to identify the changes and avoiding conflicts of overwritting any uncommitted data

